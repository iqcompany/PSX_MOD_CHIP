;------------------------------------------------------------------------
; PSX MOD CHIP for PIC10F202
; PIC12C508版からの移植
; - 内蔵4MHz RC発振器使用（外付けクリスタル不要）
; - 4.45MHz補正コード削除（内蔵発振器は4MHz固定）
;------------------------------------------------------------------------
	list	P=10F202
	radix	dec
	include	"p10f202.inc"
;------------------------------------------------------------------------
	__CONFIG _MCLRE_OFF & _CP_OFF & _WDT_OFF & _IntRC_OSC
;------------------------------------------------------------------------
; RAMの変数 (汎用レジスタ: PIC10F202は0x08から)
;------------------------------------------------------------------------
	cblock	0x08
		i			; ループカウンタ(ブロック/外側)
		j			; ループカウンタ(バイト数)
		k			; ループカウンタ(ビット数)
		x			; ディレイmsカウンタ
		y			; ディレイ内側ループカウンタ
		xmit			; 送信データバッファ
		index			; ルックアップテーブルインデックス
	endc
;------------------------------------------------------------------------
	org	0x00
;------------------------------------------------------------------------
	movwf	OSCCAL			; オシレータキャリブレーション値を保存
	goto	start			; メインへジャンプ
;------------------------------------------------------------------------
; サブルーチン: 50msディレイ
;------------------------------------------------------------------------
dly50	movlw	50			; 50 -> W
;------------------------------------------------------------------------
; サブルーチン: W msディレイ
; 引数: W = ディレイ(ms)
; 戻値: W = 3
;------------------------------------------------------------------------
dly_ms	movwf	x			; W -> x (msカウンタ)
dy_0	movlw	249			; 1msのループカウンタ(249)
	movwf	y			; 249 -> y
dy_1	nop				; 4サイクル × 249 = 996 ≒ 1ms
	decfsz	y,F			; yをデクリメント、0ならスキップ
	goto	dy_1			; 内側ループ
	decfsz	x,F			; xをデクリメント、0ならスキップ
	goto	dy_0			; 外側ループ
	retlw	3			; 3 -> W してリターン
;------------------------------------------------------------------------
; サブルーチン: Wブロック × 4バイトを送信
;   ブロック先頭に72msのウエイト
;   LSBファースト、4ms/bit、1スタートビット、2ストップビット
;   信号はLow時はLow出力、High時はポートをハイインピーダンスにする
; 引数: W = ブロック数
; 戻値: W = 3
;------------------------------------------------------------------------
sendln	movwf	i			; W -> ブロックカウンタ
sl_0	movlw	72			; 72msのウエイト
	call	dly_ms			; ディレイ
	movlw	4			; 1ブロック4バイト
	movwf	j			; 4 -> バイトカウンタ
sl_1	movf	index,W			; テーブルインデックス -> W
	call	lines			; テーブルからデータを取得
	movwf	xmit			; 送信バッファへ格納
	comf	xmit,F			; 送信用にインバート

	movlw	8			; 8ビット
	movwf	k			; 8 -> ビットカウンタ
	movlw	b'11111011'		; スタートビット(GP2=出力, GP1=Hi-Z)
	tris	GPIO			; ポート方向を設定
	movlw	4			; 4ms/bit
	call	dly_ms			; ディレイ

sl_2	rrf	xmit,F			; 右ローテート、LSB -> Carry
	movlw	b'11111001'		; GP1=GP2=出力(ロウ)
	movwf	GPIO			; GP1=0, GP2=0
	btfsc	STATUS,C		; C=0ならスキップ
	movlw	b'11111011'		; C=1: GP2=出力, GP1=Hi-Z
	btfss	STATUS,C		; C=1ならスキップ
	movlw	b'11111001'		; C=0: GP1=GP2=出力
	tris	GPIO			; ポート方向を設定
	movlw	4			; 4ms/bit
	call	dly_ms			; ディレイ
	decfsz	k,F			; 全8ビット送信したか
	goto	sl_2			; まだなら次のビットへ
	movlw	b'11111001'		; ストップビット(GP1=GP2=出力)
	tris	GPIO			; ポート方向を設定
	movlw	8			; 8ms(2ストップビット分)
	call	dly_ms			; ディレイ
	incf	index,F			; テーブルインデックスをインクリメント
	decfsz	j,F			; 4バイト送信したか
	goto	sl_1			; まだなら次のバイトへ
	decfsz	i,F			; 全ブロック送信したか
	goto	sl_0			; まだなら次のブロックへ
	retlw	3			; 3 -> W してリターン
;------------------------------------------------------------------------
; ルックアップテーブル: 地域コード文字列
;------------------------------------------------------------------------
lines	addwf	PCL,F			; 計算ジャンプ
	dt	'S','C','E','I'		; 日本/NTSC
	dt	'S','C','E','A'		; アメリカ/NTSC
	dt	'S','C','E','E'		; ヨーロッパ/PAL
;------------------------------------------------------------------------
; メインプログラム
;------------------------------------------------------------------------
	org	0x0100
start	movlw	b'11000010'		; TMR0プリスケーラを1:8へ
	option				; オプションレジスタを設定
	movlw	b'11111111'		; 全ピンHi-Z
	tris	GPIO			; ポート方向を設定
;
; リセット後約50ms: GP1をロウにする
;
	call	dly50			; 50ms待ち
	bcf	GPIO,1			; GP1をロウに
	movlw	b'11111101'		; GP1=出力
	tris	GPIO			; ポート方向を設定
;
; 約850ms後: GP2をロウにする
;
	movlw	17			; 17 * 50ms = 850ms
	movwf	i			; 17 -> カウンタ
m01	call	dly50			; 50ms待ち
	decfsz	i,F			; デクリメント
	goto	m01			; ループ
	bcf	GPIO,2			; GP2をロウに
	movlw	b'11111001'		; GP1=GP2=出力
	tris	GPIO			; ポート方向を設定
;
; 約314ms後: データ送信開始
;
	movlw	6			; 6 * 50ms = 300ms
	movwf	i			; 6 -> カウンタ
m02	call	dly50			; 50ms待ち
	decfsz	i,F			; デクリメント
	goto	m02			; ループ
	movlw	14			; 14ms
	call	dly_ms			; ディレイ (retlw 3 → W=3)
;
; 送信ループ(無限)
;
m03	clrf	index			; テーブルインデックスをリセット
	call	sendln			; データを送信 (W=3 → 3ブロック)
	goto	m03			; 繰り返し
	end
