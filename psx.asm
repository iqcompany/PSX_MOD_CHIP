;------------------------------------------------------------------------
; PIC12C508 逆アセンブリ (psx.hexより)
;------------------------------------------------------------------------
	list	P=12C508
	radix	dec
	include	"p12c508.inc"
;------------------------------------------------------------------------
	__FUSES	_MCLRE_OFF & _CP_OFF & _WDT_OFF & _XT_OSC
;------------------------------------------------------------------------
; RAMの変数 (汎用レジスタ)
;------------------------------------------------------------------------
	cblock	0x07
		reg07			; ループカウンタ(ブロック/外側)
		reg08			; ループカウンタ(バイト数)
		reg09			; ループカウンタ(ビット数)
		reg0A			; ディレイmsカウンタ
		reg0B			; ディレイ内側ループカウンタ
		reg0C			; 送信データバッファ
		reg0D			; ルックアップテーブルインデックス
		reg0E			; モードフラグ
	endc
;------------------------------------------------------------------------
	org	0x00
;------------------------------------------------------------------------
	movwf	OSCCAL			; オシレータキャリブレーション値を保存
	goto	start			; メインへジャンプ
;------------------------------------------------------------------------
; サブルーチン: 50msディレイ
;------------------------------------------------------------------------
sub_02	movlw	50			; 50 -> W
;------------------------------------------------------------------------
; サブルーチン: W msディレイ
;------------------------------------------------------------------------
sub_03	movwf	reg0A			; W -> reg0A (msカウンタ)
L_004	movlw	249			; 1msのループカウンタ(249)
	movwf	reg0B			; 249 -> reg0B
L_006	nop				; 4 * 249 = 996サイクル
	decfsz	reg0B,F			; デクリメント、0ならスキップ
	goto	L_006			; 内側ループ
	decfsz	reg0A,F			; msカウンタをデクリメント
	goto	L_004			; 外側ループ
	btfss	reg0E,0			; モードのビット0を確認
	retlw	3			; mode<0>=0なら 3を返す
	movlw	36			; 4.45MHz用の追加ディレイ
	movwf	reg0B			; 36 -> reg0B
L_00F	decfsz	reg0B,F			; デクリメント
	goto	L_00F			; ループ
	retlw	3			; 3を返す
;------------------------------------------------------------------------
; サブルーチン: Wブロック×4バイトを送信
;   ブロック先頭に72msのウエイト、LSBファースト、4ms/bit
;   1スタートビット、2ストップビット
;------------------------------------------------------------------------
sub_12	movwf	reg07			; W -> ブロックカウンタ
L_013	movlw	72			; 72msのウエイト
	call	sub_03			; ディレイ
	movlw	4			; 1ブロック4バイト
	movwf	reg08			; 4 -> バイトカウンタ
L_017	movf	reg0D,W			; テーブルインデックス -> W
	call	tbl_37			; テーブルからデータを取得
	movwf	reg0C			; 送信バッファへ格納
	comf	reg0C,F			; 送信用にインバート

	movlw	8			; 8ビット
	movwf	reg09			; 8 -> ビットカウンタ
	movlw	b'11111011'		; スタートビット(GP2=出力, GP1=Hi-Z)
	tris	GPIO			; ポート方向を設定
	movlw	4			; 4ms/bit
	call	sub_03			; ディレイ

L_021	rrf	reg0C,F			; 右ローテート、ビット -> Carry
	movlw	b'11111001'		; GP1=GP2=出力(ロウ)
	movwf	GPIO			; GP1=0, GP2=0
	btfsc	STATUS,C		; C=0ならスキップ
	movlw	b'11111011'		; C=1: GP2=出力, GP1=Hi-Z
	btfss	STATUS,C		; C=1ならスキップ
	movlw	b'11111001'		; C=0: GP1=GP2=出力
	tris	GPIO			; ポート方向を設定
	movlw	4			; 4ms/bit
	call	sub_03			; ディレイ
	decfsz	reg09,F			; 全8ビット送信したか
	goto	L_021			; まだなら次のビットへ
	movlw	b'11111001'		; ストップビット(GP1=GP2=出力)
	tris	GPIO			; ポート方向を設定
	movlw	8			; 8ms(2ストップビット分)
	call	sub_03			; ディレイ
	incf	reg0D,F			; テーブルインデックスをインクリメント
	decfsz	reg08,F			; 4バイト送信したか
	goto	L_017			; まだなら次のバイトへ
	decfsz	reg07,F			; 全ブロック送信したか
	goto	L_013			; まだなら次のブロックへ
	retlw	3			; 3を返す
;------------------------------------------------------------------------
; ルックアップテーブル: 地域コード文字列
;------------------------------------------------------------------------
tbl_37	addwf	PCL,F			; 計算ジャンプ
	dt	'S','C','E','I'		; 日本/NTSC
	dt	'S','C','E','A'		; アメリカ/NTSC
	dt	'S','C','E','E'		; ヨーロッパ/PAL
;------------------------------------------------------------------------
; メインプログラム
;------------------------------------------------------------------------
	org	0x0100
start	movlw	b'11000010'		; TMR0プリスケーラを1:8へ
	option				; オプションレジスタを設定
	movlw	b'11111111'		; 全ピンHi-Z
	tris	GPIO			; ポート方向を設定
	clrf	reg0E			; モードフラグをクリア
;
; リセット後約50ms: GP1をロウにする
;
	call	sub_02			; 50ms待ち
	bcf	GPIO,1			; GP1をロウに
	movlw	b'11111101'		; GP1=出力
	tris	GPIO			; ポート方向を設定
;
; 約850ms後: GP2をロウにする
;
	movlw	17			; 17 * 50ms = 850ms
	movwf	reg07			; 17 -> カウンタ
L_10B	call	sub_02			; 50ms待ち
	decfsz	reg07,F			; デクリメント
	goto	L_10B			; ループ
	bcf	GPIO,2			; GP2をロウに
	movlw	b'11111001'		; GP1=GP2=出力
	tris	GPIO			; ポート方向を設定
;
; 約314ms後: データ送信開始
;
	movlw	6			; 6 * 50ms = 300ms
	movwf	reg07			; 6 -> カウンタ
L_113	call	sub_02			; 50ms待ち
	decfsz	reg07,F			; デクリメント
	goto	L_113			; ループ
	movlw	14			; 14ms
	call	sub_03			; ディレイ
;
; 送信ループ(無限)
;
L_118	clrf	reg0D			; テーブルインデックスをリセット
	call	sub_12			; データを送信
	incf	reg0E,F			; モードをインクリメント
	goto	L_118			; 繰り返し
	end
